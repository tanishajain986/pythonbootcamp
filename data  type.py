# Example program to demonstrate various data types in Python

# Integer
integer_var = 42
print(f"Value: {integer_var}, Type: {type(integer_var)}")

# Float
float_var = 3.14
print(f"Value: {float_var}, Type: {type(float_var)}")

# String
string_var = "Hello, world!"
print(f"Value: '{string_var}', Type: {type(string_var)}")

# Boolean
boolean_var = True
print(f"Value: {boolean_var}, Type: {type(boolean_var)}")

# List
list_var = [1, 2, 3, 4, 5]
print(f"Value: {list_var}, Type: {type(list_var)}")

# Tuple
tuple_var = (1, 2, 3, 4, 5)
print(f"Value: {tuple_var}, Type: {type(tuple_var)}")

# Dictionary
dict_var = {'name': 'Alice', 'age': 30}
print(f"Value: {dict_var}, Type: {type(dict_var)}")

# Set
set_var = {1, 2, 3, 4, 5}
print(f"Value: {set_var}, Type: {type(set_var)}")

# Complex Number
complex_var = 2 + 3j
print(f"Value: {complex_var}, Type: {type(complex_var)}")
